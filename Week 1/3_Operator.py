######OPERATORS###############
print(3//2)  #Divide(Quotient) Operator
print(2*3)
print(4**2) #Power Operator
print(4%2)  #Remainder Operator
print(9+3)
print(8-6)

print(1==2)
print(2<1)
print(1!=1)
print(1!=2)
print(2<=6)
print(True and False)  #In AND Truth table both should be True than result will be True
print(True and True)
print(True or False)   #In OR Truth table both should be False than result will be False
print(False or False)
print(not True)

######## AND OPERATOR ############## 
print(10&10) #Bitwise Operator
print(bin(10))  #Binary equivalent

print(23&18) #Bitwise Operator
print(bin(23))  #Binary equivalent
print(bin(18))   #binary equivalent

print(2&3)
print(bin(2))
print(bin(3))


###### OR OPERATOR #############
print(10 | 10) #Bitwise Operator
print(bin(10))  #Binary equivalent

print(23 | 18) #Bitwise Operator
print(bin(23))  #Binary equivalent
print(bin(18))   #binary equivalent

print(2 | 3)
print(bin(2))
print(bin(3))

###### NEGATION #########
print(~34)

######### RIGHT SHIFT ######
print(34 >> 4)  #IT WILL LOSE FOUR ELEMENTS FROM RIGHT
print(bin(34))
print(bin(2))

######### LEFT SHIFT ######
print(34 << 4)  #IT WILL GAIN FOUR ELEMENTS(0) FROM RIGHT
print(bin(34))
print(bin(544))

######## ASSIGNMENT OPERATOR ###############
a=10
print(a+2)   #Both steps will perform addition to the (a) value
a += 2
print(a)

print(a-2)    #Both steps will perform subtraction to the current (a) value
a -= 2
print(a)